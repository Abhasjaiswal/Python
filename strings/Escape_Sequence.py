"""Escape Sequence Operator"""
"""1.Escape sequence is used to insert a non-allowed character in a string"""
"""2.Escape Character consists of escape operator(\)followed by a non-allowed character"""

#For Example
print("I am Abhas and I am from \"India\"")


#3.Some escape characters includes \n , \b , \t, \\, \ooo and \xhh

#For Example
print("I am Abhas and \nI am \x66rom \"India\"")


#The output is 
#I am Abhas and I am from "India"
#I am Abhas and 
#I am from "India"
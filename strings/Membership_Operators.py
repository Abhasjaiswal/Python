#Membership Operators

"""1.The IN Operator"""
"""The in operator is used to check if a value exists in a sequence, such as a string, list, tuple, or set."""

#for example
string="Abhas"
print('Ab'in string)
#Since Ab is present in the string "Abhas" therefore the output will be TRUE

"""2.The NOT IN operator"""
"""The not in operator is used to check if a value does not exist in a sequence."""

#for example
string="Hello"
print('Ab'not in string)
#Since 'Ab' is not present in the string therefore it will produce TRUE
